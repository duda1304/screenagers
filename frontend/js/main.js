const socket = io();

socket.on('step', (data) => {
    $('#console_section').css({ 
        'top' : data.top,
        'left' : data.left,
        'width' : data.width, 
        'height' : data.height 
    })
})


socket.on('console', (data) => {
    if ('active' in data && data.active === false) {
        $('#console_section').hide();
    } else {
        $('#console_section').show();
    }
})


